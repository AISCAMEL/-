  /** URLスラッグ（/services/[slug]） */
  slug: string;
  /** 短いラベル（ナビ・カード見出し） */
  name: string;
  /** カードのサブコピー */
  tagline: string;
  /** トップページ等での要約 */
  summary: string;
  /** アイコン識別子（components/ui/Icon.tsx で描画） */
