export function renderTemplate(tpl: string, vars: { name?: string | null; company?: string | null }): string {
  return tpl
    .replace(/\{\{name\}\}/g, vars.name?.trim() || 'ご担当者')
    .replace(/\{\{company\}\}/g, vars.company?.trim() || '');
}
